require("include")

function m300_090()
  Fade_Out(0, 0)
  EVENT_BEGIN()
  Talk.Load("m300")
  SetEventCutNo(2)
  if Debug.IsEventView() then
    SetTimeZone("Noon")
    SetMapStatus("Enable")
    SetTimeAxis("P2028_daft_300")
    LoadMap("d0475b")
  end
  LoadModel("chr179_01", "char_BACCHUSMON")
  LoadModel("chr232_01", "char_SIRENMON")
  LoadSceneViewerResource("c1.0", "d0475b_ta")
  LoadSceneViewerResource("c2.0", "d0475b_ta")
  LoadEnvironmentFile("c1.0", "es_m300_090_c1.0")
  LoadEnvironmentFile("c2.0", "es_m300_090_c2.0")
  WaitVistaLoad()
  FIELD_SET()
  EVENT_START()
  CUT("1.0")
  SetEnv_All("c1.0", 0)
  SetPosition("chr179_01", -1.78, 4.44, 9.471, "LINEAR", 0, 0)
  SetRotation("chr179_01", 27.5, 184.5, 0, "LINEAR", 0, 0)
  SetPosition("chr232_01", -5.941, 0, 0.081, "LINEAR", 0, 0)
  SetRotation("chr232_01", 0, 81, 0, "LINEAR", 0, 0)
  PlayMotion("chr179_01", "bn01", 0, true, 0)
  SetFace("chr179_01", "F01_M01", 0, 1, 0, 0)
  PlayMotion("chr232_01", "bn01", 0, true, 0)
  ChangeDigimonFace("chr232_01", EYE_EXPRESSION_NORMAL, 0)
  SetMouth("chr232_01", 1, 0, 0)
  BlendFaceDigi("chr179_01", 5, 15, 0, "SCURVE", 0, 0)
  BlendFaceDigi("chr232_01", -5, -22, 0, "SCURVE", 0, 0)
  SetCamera(-5.087, 1.273, -5.965, 0, 20, "LINEAR", 0, false, 0)
  SetCameraTarget(-3.043, 7.368, 1.925, "LINEAR", 0, false, 0)
  Fade_In(1, FADE_TIME)
  if Common.IsVoiceJapanese() then
    SetMouth("chr179_01", 0, 6, 0)
    SetMouth("chr179_01", 2, 6, 45)
    SetMouth("chr179_01", 0, 6, 75)
    SetMouth("chr179_01", 2, 6, 135)
  else
    SetMouth("chr179_01", 0, 6, 0)
    SetMouth("chr179_01", 2, 6, 17)
    SetMouth("chr179_01", 0, 6, 33)
    SetMouth("chr179_01", 2, 6, 77)
  end
  MessageTalk("m300_090_010")
  MessageClose()
  WaitFrame(0)
  SetMouth("chr179_01", 2, 8, 0)
  WaitFrame(10)
  if Common.IsVoiceJapanese() then
    SetMouth("chr179_01", 0, 6, 0)
    SetMouth("chr179_01", 2, 6, 14)
  else
    SetMouth("chr179_01", 0, 6, 0)
    SetMouth("chr179_01", 2, 6, 14)
  end
  MessageTalk("m300_090_020")
  MessageClose()
  WaitFrame(0)
  SetMouth("chr179_01", 2, 8, 0)
  WaitFrame(10)
  CUT("2.0")
  SetEnv_All("c2.0", 0)
  SetPosition("chr179_01", -1.78, 0, 5.656, "LINEAR", 0, 0)
  SetRotation("chr179_01", 0, 184.5, 0, "LINEAR", 0, 0)
  SetPosition("chr232_01", -5.941, 0, 0.081, "LINEAR", 0, 0)
  SetRotation("chr232_01", 0, 156.83, 0, "LINEAR", 0, 0)
  BlendFaceDigi("chr179_01", 5, 15, 0, "SCURVE", 0, 0)
  BlendFaceDigi("chr232_01", -5, -22, 0, "SCURVE", 0, 0)
  SetCamera(3.309, 7.163, 2.638, 0, 30, "LINEAR", 0, false, 0)
  SetCameraTarget(-7.926, 0.681, 2.333, "LINEAR", 0, false, 0)
  WaitFrame(5)
  PlayMotion("chr232_01", "fq02", 10, false, 0)
  PlayMotion("chr232_01", "bn01", 0, true, 50)
  WaitFrame(20)
  MessageTalk("m300_090_030")
  MessageClose()
  WaitFrame(10)
  Fade_OutNoLoadingWithWait(1, 2)
  WaitFrame(10)
  Vista.SetKeepBGM()
  EVENT_END()
end

function CUT(cut_no)
  CUT_START(cut_no)
end

function main()
  m300_090()
end
