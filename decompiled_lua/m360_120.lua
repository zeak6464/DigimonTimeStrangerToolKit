require("include")

function m360_120()
  Fade_Out(1, 0)
  EVENT_BEGIN()
  Talk.Load("m360")
  SetEventCutNo(2)
  if Debug.IsEventView() then
    SetTimeZone("Night")
    SetTimeAxis("Z2028_daft_360")
    LoadMap("t3001f")
  end
  LoadModel("HERO_01", "Player")
  LoadModel("MIREI_01", "char_MIREI_CHILD")
  LoadSceneViewerResource("c1.0", "t3001f_pn")
  LoadSceneViewerResource("c2.0", "t3001f_pn")
  LoadEnvironmentFile("c1.0", "es_m360_120_c1.0")
  LoadEnvironmentFile("c2.0", "es_m360_120_c2.0")
  WaitVistaLoad()
  SetPosition("HERO_01", 0.07, 0, -9.849, "LINEAR", 0, 0)
  SetRotation("HERO_01", 0, 0, 0, "LINEAR", 0, 0)
  SetScale("HERO_01", 1, 1, 1)
  SetPosition("MIREI_01", 0.028, 0, -7.328, "LINEAR", 0, 0)
  SetRotation("MIREI_01", 0, 180, 0, "LINEAR", 0, 0)
  SetScale("MIREI_01", 1, 1, 1)
  PlayMotion("HERO_01", "fn01_01", 0, true, 0)
  SetFace("HERO_01", "F01", 0, 1, FACE_BLENDTIME, 0)
  PlayMotion("MIREI_01", "e002", 0, true, 0)
  SetFace("MIREI_01", "F01", 0, 1, FACE_BLENDTIME, 0)
  BlendEye("HERO_01", 0, 0, "SCURVE", 0, 0)
  BlendFace("HERO_01", 0, 0, 0, false, "SCURVE", 0, 0)
  BlendEye("MIREI_01", -30, 0, "SCURVE", 0, 0)
  BlendFace("MIREI_01", -19, 0, 0, false, "SCURVE", 0, 0)
  SetCamera(0.328, 1.809, -10.937, 0, 25, "LINEAR", 0, false, 0)
  SetCameraTarget(-0.266, 0.78, -7.202, "LINEAR", 0, false, 0)
  SetCamera(0.591, 1.857, -10.864, 0, 25, "LINEAR", 180, true, 0)
  SetCameraTarget(-0.705, 0.724, -7.342, "LINEAR", 180, true, 0)
  Event_PlayBGM(114, 0, 0)
  FIELD_SET()
  EVENT_START()
  CUT("1.0")
  WaitFrame(60)
  SetEnv_All("c1.0", 0)
  MovePosition("MIREI_01", 0.028, 0, -8.428, false, 0.4, 0)
  Fade_In(1, 60)
  BlendEye("MIREI_01", -30, 0, "SCURVE", 30, 0)
  BlendFace("MIREI_01", -37, 0, 0, false, "SCURVE", 30, 0)
  BlendEye("HERO_01", 0, 0, "SCURVE", 0, 0)
  BlendFace("HERO_01", 16, 0, 0, false, "SCURVE", 40, 0)
  MOVE_WAIT("MIREI_01")
  WaitFrame(10)
  PlayMotion("MIREI_01", "e002", 5, true, 0)
  SetFace("MIREI_01", "F01", 0, 1, FACE_BLENDTIME, 0)
  MessageTalk("m360_120_040")
  MessageClose()
  WaitFrame(10)
  CUT("2.0")
  SetEnv_All("c2.0", 0)
  SetCamera(0.398, 1.452, -9.402, 0, 25, "LINEAR", 0, false, 0)
  SetCameraTarget(-0.156, 0.997, -8.045, "LINEAR", 0, false, 0)
  MessageTalk("m360_120_050")
  MessageClose()
  WaitFrame(10)
  PlayMotion("MIREI_01", "e003", 5, false, 0)
  PlayMotion("MIREI_01", "fn01_01", 3, true, 100)
  SetFace("MIREI_01", "F01", 0, 1, FACE_BLENDTIME, 0)
  MessageTalk("m360_120_060")
  MessageClose()
  WaitFrame(10)
  MessageTalk("m360_120_070")
  MessageClose()
  WaitFrame(10)
  PlayMotionBlend("MIREI_01", "blend02", 3, false, 0)
  SetFace("MIREI_01", "F01", 0, 1, FACE_BLENDTIME, 0)
  MessageTalk("m360_120_080")
  MessageClose()
  WaitFrame(10)
  Fade_OutNoLoadingWithWait(FADE_BLACK, FADE_TIME)
  WaitFrame(60)
  EVENT_END()
end

function CUT(cut_no)
  CUT_START(cut_no)
end

function main()
  m360_120()
end
