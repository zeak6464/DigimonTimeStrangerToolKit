require("include")

function m120_080()
  Fade_Out(0, 0)
  EVENT_BEGIN()
  Talk.Load("m120")
  Vista.LoadCRILipSyncs()
  SetEventCutNo(2)
  if Debug.IsEventView() then
    SetTimeZone("Noon")
    SetTimeAxis("A2020_dbef_120")
    LoadMap("d0204")
  end
  LoadModel("HERO_01", "Player")
  LoadModel("chr718_01", "char_KNIGHTMON")
  LoadSceneViewerResource("c1.0", "d0204f_sa")
  LoadSceneViewerResource("c2.0", "d0204f_sa")
  LoadEnvironmentFile("c1.0", "es_m120_080_c1.0")
  LoadEnvironmentFile("c2.0", "es_m120_080_c2.0")
  WaitVistaLoad()
  SetPosition("HERO_01", 0.48, 0, -2.85, "LINEAR", 0, 0)
  SetRotation("HERO_01", 0, 145, 0, "LINEAR", 0, 0)
  SetPosition("chr718_01", 1.467, 0, -4.396, "LINEAR", 0, 0)
  SetRotation("chr718_01", 0, -30, 0, "LINEAR", 0, 0)
  PlayMotion("HERO_01", "fn01_01", 0, true, 0)
  SetFace("HERO_01", "F01", 0, 1, 0, 0)
  PlayMotion("chr718_01", "e021", 0, false, 0)
  SetStartFrame("chr718_01", "e021", 0, 0)
  SetLoopFrame("chr718_01", "e021", 0, 70)
  PlayMotion("chr718_01", "f000", 15, true, 70)
  BlendEye("HERO_01", -10, 0, "SCURVE", 0, 0)
  BlendFace("HERO_01", -42, 0, 0, true, "SCURVE", 0, 0)
  BlendFaceDigi("chr718_01", 28, -8, 0, "LINEAR", 0, 0)
  SetCamera(0.746, 1.762, -2.859, 0, 40, "LINEAR", 0, false, 0)
  SetCameraTarget(1.401, 1.963, -4.392, "LINEAR", 0, false, 0)
  SetEnv_All("c1.0", 0)
  Field_InvisiblePlayer(INVISIBLE_KEY_EVENT, 0, false)
  Field_InvisibleFollowerAllGuest(INVISIBLE_KEY_EVENT, 0, false)
  Field_InvisibleFollowerAllPartyMember(INVISIBLE_KEY_EVENT, 0, false)
  Field_InvisibleAllSymbolEnemy(INVISIBLE_KEY_EVENT, 0, false)
  Field_InvisibleNpc("npc_1001", INVISIBLE_KEY_EVENT, 0, true)
  Field_InvisibleRumorNpc(FOR_ALL, INVISIBLE_KEY_EVENT, 0, true)
  EVENT_START()
  CUT("1.0")
  Fade_In(0, FADE_TIME)
  WaitFrame(FADE_TIME)
  WaitFrame(10)
  MessageTalk("m120_080_020")
  MessageClose()
  WaitFrame(10)
  correct = false
  while not Vista.IsSkipping() and not correct do
    CUT("2.0")
    SetEnv_All("c2.0", 0)
    SetCamera(0.538, 1.535, -1.89, 0, 40, "LINEAR", 0, false, 0)
    SetCameraTarget(1.125, 1.71, -4.996, "LINEAR", 0, false, 0)
    BlendEye("HERO_01", -10, 0, "SCURVE", 0, 0)
    BlendFace("HERO_01", -42, 0, 0, true, "SCURVE", 0, 0)
    MessageTalkSel(3, "m120_080_030")
    result = Talk.GetResultSelectedNum()
    if result == RESULT_TALK02 then
      PlayMotionForce("HERO_01", "e002", 10, true, 0)
      SetFace("HERO_01", "E01_M01", 0, 0, FACE_BLENDTIME, 0)
      SetFace("HERO_01", "E01_M01", 0, 1, FACE_BLENDTIME, 70)
      WaitFrame(80)
      EndMotion("HERO_01", 0)
      CUT("2.0_A1")
      SetEnv_All("c1.0", 0)
      SetCamera(0.746, 1.762, -2.859, 0, 40, "LINEAR", 0, false, 0)
      SetCameraTarget(1.401, 1.963, -4.392, "LINEAR", 0, false, 0)
      MessageTalk("m120_080_040")
      MessageClose()
      WaitFrame(10)
      PlayMotion("chr718_01", "fq04", 4, false, 0)
      PlayMotion("chr718_01", "f000", 4, true, 50)
      MessageTalk("m120_080_050")
      MessageClose()
      WaitFrame(5)
    elseif result == RESULT_TALK01 then
      PlayMotionForce("HERO_01", "e002", 10, true, 0)
      SetFace("HERO_01", "E01_M01", 0, 0, FACE_BLENDTIME, 0)
      SetFace("HERO_01", "E01_M01", 0, 1, FACE_BLENDTIME, 70)
      WaitFrame(80)
      EndMotion("HERO_01", 0)
      CUT("2.0_B1")
      SetEnv_All("c1.0", 0)
      SetCamera(0.746, 1.762, -2.859, 0, 40, "LINEAR", 0, false, 0)
      SetCameraTarget(1.401, 1.963, -4.392, "LINEAR", 0, false, 0)
      MessageTalk("m120_080_060")
      PlayMotion("chr718_01", "fq04", 4, false, 0)
      PlayMotion("chr718_01", "f000", 4, true, 50)
      MessageTalk("m120_080_070")
      PlayMotion("chr718_01", "e021", 4, false, 0)
      SetStartFrame("chr718_01", "e021", 4, 0)
      SetLoopFrame("chr718_01", "e021", 4, 70)
      PlayMotion("chr718_01", "f000", 15, true, 70)
      MessageTalk("m120_080_080")
      MessageClose()
      WaitFrame(5)
    elseif result == RESULT_TALK00 then
      PlayMotion("HERO_01", "e002", 10, true, 0)
      PlayMotionBlend("HERO_01", "blend01", 0, false, 0)
      SetFace("HERO_01", "E01_M01", 0, 0, FACE_BLENDTIME, 0)
      SetFace("HERO_01", "E01_M01", 0, 1, FACE_BLENDTIME, 70)
      WaitFrame(80)
      EndMotion("HERO_01", 0)
      CUT("2.0_C1")
      if Flg.Check("FLAG_MAIN_03_590") then
        Flg.Set("FLAG_MAIN_03_625")
      end
      SetEnv_All("c1.0", 0)
      SetCamera(0.746, 1.762, -2.859, 0, 40, "LINEAR", 0, false, 0)
      SetCameraTarget(1.401, 1.963, -4.392, "LINEAR", 0, false, 0)
      MessageTalk("m120_080_090")
      MessageTalk("m120_080_100")
      MessageTalk("m120_080_110")
      MessageClose()
      WaitFrame(15)
      correct = true
    end
    BlendEye("HERO_01", 0, 0, "SCURVE", 10, 0)
    BlendFace("HERO_01", 0, 0, 0, true, "SCURVE", 30, 0)
    BlendFaceDigi("chr718_01", 0, 0, 0, "SCURVE", 30, 0)
    EndMotion("chr718_01", 0)
    WaitFrame(40)
  end
  if Flg.Check("FLAG_MAIN_03_625") then
    BackFadeOutWithWait(0, FADE_TIME)
  end
  if Debug.IsEventView() then
    EVENT_END()
  else
    EVENT_END_SEAMLESS(60)
  end
end

function CUT(cut_no)
  CUT_START(cut_no)
end

function main()
  m120_080()
end
