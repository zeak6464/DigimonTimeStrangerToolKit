  SetRotation("AEGIOMON_01", 0, 180, 0, "LINEAR", 0, 0)
  SetPosition("HERO_01", 0.562, 0.375, -1.4, "LINEAR", 0, 0)
  SetRotation("HERO_01", 0, 180, 0, "LINEAR", 0, 0)
  SetPosition("HEROINE_01", -0.611, 0.375, -1.4, "LINEAR", 0, 0)
  SetRotation("HEROINE_01", 0, 180, 0, "LINEAR", 0, 0)
  PlayMotion("HERO_01", "e050", 0, false, 0)
  PlayMotion("HERO_01", "fs01_01", 3, true, 112)
