    Field.ObjectPlayMotion(OGIM, GetIndex(OGIM, "obj_movechair01"), "e002", 0, false)
    Field.ObjectPlayMotion(OGIM, GetIndex(OGIM, "obj_movechair02"), "e002", 0, false)
    Field.ObjectPlayMotion(OGIM, GetIndex(OGIM, "obj_movechair03"), "e002", 0, false)
  end
