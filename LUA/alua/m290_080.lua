  PlayMotion("AEGIOMON_01", "fs01_01", 3, true, 78)
  SetFace("AEGIOMON_01", "F15", 0, 1, FACE_BLENDTIME, 0)
  PlayMotion("HEROINE_01", "e050", 0, false, 0)
  PlayMotion("HEROINE_01", "fs01_01", 3, true, 86)
