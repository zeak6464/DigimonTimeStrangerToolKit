  BlendEye("AEGIOMON_01", -16, 0, "SCURVE", 30, 0)
  BlendFace("AEGIOMON_01", -50, 0, 0, false, "SCURVE", 30, 0)
  BlendEye("HERO_01", -8, 0, "SCURVE", 30, 0)
