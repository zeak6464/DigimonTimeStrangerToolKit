  SetEnv_All("c1.0", 0)
  Fade_In(0, FADE_TIME)
  WaitFrame(120)
  Event_StopBGM(1, 0)
  Fade_OutNoLoadingWithWait(FADE_WHITE, 60)
  Vista.SetKeepBGM()
  if not Debug.IsEventView() then
    Field.ObjectPlayMotion(OGIM, GetIndex(OGIM, "obj_movechair01"), "", 0, false)
    Field.ObjectPlayMotion(OGIM, GetIndex(OGIM, "obj_movechair02"), "", 0, false)
    Field.ObjectPlayMotion(OGIM, GetIndex(OGIM, "obj_movechair03"), "", 0, false)
