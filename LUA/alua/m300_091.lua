require("include")

function m300_091()
  Fade_Out(0, 0)
  EVENT_BEGIN()
  Talk.Load("m300")
  SetEventCutNo(3)
  if Debug.IsEventView() then
    SetTimeZone("Noon")
    SetMapStatus("Enable")
    SetTimeAxis("P2028_daft_300")
    LoadMap("d0475b")
  end
  LoadModel("chr179_01", "char_BACCHUSMON")
  LoadModel("chr232_01", "char_SIRENMON")
  LoadSceneViewerResource("c1.0", "d0475b_ta")
  LoadSceneViewerResource("c2.0", "d0475b_ta")
  LoadSceneViewerResource("c3.0", "d0475b_ta")
  LoadEnvironmentFile("c1.0", "es_m300_091_c1.0")
  LoadEnvironmentFile("c2.0", "es_m300_091_c2.0")
  LoadEnvironmentFile("c3.0", "es_m300_091_c3.0")
  WaitVistaLoad()
  FIELD_SET()
  EVENT_START()
  CUT("1.0")
  SetEnv_All("c1.0", 0)
  PlayMotion("chr179_01", "bn01", 0, true, 0)
  ChangeDigimonFace("chr179_01", EYE_EXPRESSION_NORMAL, 0)
  SetMouth("chr179_01", 1, 0, 0)
  PlayMotion("chr232_01", "bn01", 0, true, 0)
  ChangeDigimonFace("chr232_01", EYE_EXPRESSION_NORMAL, 0)
  SetMouth("chr232_01", 1, 0, 0)
  SetFace("chr232_01", "F03_M03", 0, 1, FACE_BLENDTIME, 0)
  SetPosition("chr179_01", -1.78, 0, 5.656, "LINEAR", 0, 0)
  SetRotation("chr179_01", 0, 184.5, 0, "LINEAR", 0, 0)
  SetPosition("chr232_01", -5.941, 0, 0.081, "LINEAR", 0, 0)
  SetRotation("chr232_01", 0, 156.83, 0, "LINEAR", 0, 0)
  BlendFaceDigi("chr179_01", 5, 15, 0, "SCURVE", 0, 0)
  BlendFaceDigi("chr232_01", -5, -22, 0, "SCURVE", 0, 0)
  SetCamera(4.991, 10.348, 2.239, 0, 33, "LINEAR", 0, false, 0)
  SetCameraTarget(-7.894, 0.858, 4.008, "LINEAR", 0, false, 0)
  Fade_In(1, FADE_TIME)
  BlendFaceDigi("chr232_01", 8, -19, 0, "SCURVE", 30, 0)
  SetEyeUVOffsetDigi("chr232_01", -0.016, 0.022, "SCURVE", 0, 0)
  WaitFrame(30)
  MessageTalk("m300_090_040")
  MessageClose()
  WaitFrame(10)
  CUT("2.0")
  SetEnv_All("c2.0", 0)
  SetPosition("chr179_01", -1.78, 4.44, 9.471, "LINEAR", 0, 0)
  SetRotation("chr179_01", 27.5, 184.5, 0, "LINEAR", 0, 0)
  SetPosition("chr232_01", -5.941, 0, 0.081, "LINEAR", 0, 0)
  SetRotation("chr232_01", 0, 90, 0, "LINEAR", 0, 0)
  BlendFaceDigi("chr179_01", 5, 15, 0, "SCURVE", 0, 0)
  BlendFaceDigi("chr232_01", 5, 0, 0, "SCURVE", 30, 0)
  SetCamera(-3.252, 0.139, 1.2, 0, 30, "LINEAR", 0, false, 0)
  SetCameraTarget(-5.79, 0.539, 0.477, "LINEAR", 0, false, 0)
  WaitFrame(10)
  MessageTalk("m300_090_050")
  Event_PlaySE(400001, 80, false, 0, 0.5)
  PlayMotion("chr232_01", "e005", 10, true, 0)
  SetFace("chr232_01", "F03_M03", 2, 1, 0, 0)
  SetEyeUVOffsetDigi("chr232_01", -0.016, 0.022, "SCURVE", 0, 0)
  MessageClose()
  WaitFrame(20)
  MessageTalk("m300_090_060")
  MessageTalk("m300_090_070")
  MessageClose()
  WaitFrame(10)
  CUT("3.0")
  SetEnv_All("c3.0", 0)
  SetFace("chr179_01", "F01_M01", 0, 1, 0, 0)
  SetPosition("chr179_01", -1.78, 0, 5.656, "LINEAR", 0, 0)
  SetRotation("chr179_01", 6.5, 184.5, 0, "LINEAR", 0, 0)
  BlendFaceDigi("chr179_01", 5, 15, 0, "SCURVE", 0, 0)
  SetCamera(4.102, 7.747, -1.862, 0, 40, "LINEAR", 0, false, 0)
  SetCameraTarget(-2.021, 6.205, 3.49, "LINEAR", 0, false, 0)
  WaitFrame(10)
  if Common.IsVoiceJapanese() then
    SetMouth("chr179_01", 0, 6, 0)
    SetMouth("chr179_01", 2, 6, 45)
  else
    SetMouth("chr179_01", 0, 6, 0)
    SetMouth("chr179_01", 2, 6, 54)
  end
  MessageTalk("m300_090_080")
  MessageClose()
  WaitFrame(0)
  SetMouth("chr179_01", 2, 6, 0)
  WaitFrame(10)
  Fade_OutNoLoadingWithWait(1, 2)
  WaitFrame(10)
  Vista.SetKeepBGM()
  EVENT_END()
end

function CUT(cut_no)
  CUT_START(cut_no)
end

function main()
  m300_091()
end
