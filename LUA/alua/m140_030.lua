  PlayMotion("chr009_01", "bn01", 0, true, 50)
  MessageTalk("m140_030_040")
  MessageClose()
  WaitFrame(10)
  CUT("3.0")
  correct = false
  while not Vista.IsSkipping() and not correct do
    SetEnv_All("c3.0", 0)
    SetCamera(-0.384, 1.354, -20.615, 0, 20, "LINEAR", 0, false, 0)
    SetCameraTarget(0.625, 1.464, -19.247, "LINEAR", 0, false, 0)
    SetFace("HERO_01", "F01", 0, 1, 0, 0)
    MessageTalkSel(3, "m140_030_050")
    result = Talk.GetResultSelectedNum()
