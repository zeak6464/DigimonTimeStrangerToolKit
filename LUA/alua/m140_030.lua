      PlayMotion("HERO_01", "e002", 4, false, 0)
      SetFace("HERO_01", "F01", 0, 0, FACE_BLENDTIME, 0)
      SetFace("HERO_01", "F01", 0, 1, FACE_BLENDTIME, 60)
      WaitFrame(65)
