  end
  BlendEye("HERO_01", 0, 0, "SCURVE", 30, 0)
  BlendFace("HERO_01", 0, 0, 0, true, "SCURVE", 30, 0)
  if Debug.IsEventView() then
    EVENT_END()
  else
    EVENT_END_SEAMLESS(80)
  end
end

