      SetCameraTarget(0.324, 0.914, -21.006, "LINEAR", 0, false, 0)
      PlayMotion("chr009_01", "fe03", 2, false, 0)
      PlayMotion("chr009_01", "bn01", 0, true, 90)
      MessageTalk("m140_030_060")
      MessageClose()
      WaitFrame(10)
      PlayMotion("chr009_01", "fe01", 2, false, 0)
      PlayMotion("chr009_01", "bn01", 10, true, 50)
      MessageTalk("m140_030_070")
      MessageClose()
      WaitFrame(30)
      correct = true
    elseif result == RESULT_TALK01 then
      PlayMotionBlend("HERO_01", "blend03", 2, false, 0)
      SetFace("HERO_01", "F15", 0, 0, FACE_BLENDTIME, 0)
      SetFace("HERO_01", "F15", 0, 1, FACE_BLENDTIME, 60)
      WaitFrame(70)
      CUT("3.0_B1")
      SetEnv_All("c3.0_B1", 0)
      SetCamera(0.318, 1.073, -19.453, 0, 39, "LINEAR", 0, false, 0)
      SetCameraTarget(0.137, 0.811, -21.002, "LINEAR", 0, false, 0)
      PlayMotion("chr009_01", "fe01", 2, false, 0)
      PlayMotion("chr009_01", "bn01", 10, true, 50)
      MessageTalk("m140_030_080")
      MessageClose()
      WaitFrame(10)
      MessageTalk("m140_030_090")
      MessageClose()
      WaitFrame(10)
    elseif result == RESULT_TALK02 then
      PlayMotionBlend("HERO_01", "blend02", 2, false, 0)
      SetFace("HERO_01", "E07_M01", 0, 0, FACE_BLENDTIME, 0)
      SetFace("HERO_01", "E07_M01", 0, 1, FACE_BLENDTIME, 60)
      WaitFrame(70)
