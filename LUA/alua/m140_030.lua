  end
  LoadModel("HERO_01", "Player")
  LoadModel("chr009_01", "char_HAGURUMON")
  LoadSceneViewerResource("c1.0", "m140_030_c1.0")
  LoadSceneViewerResource("c2.0", "m140_030_c2.0")
  LoadSceneViewerResource("c3.0", "d0206f_sa")
  LoadSceneViewerResource("c3.0_A1", "d0206f_sa")
