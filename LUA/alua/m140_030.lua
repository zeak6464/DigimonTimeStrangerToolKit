  SetCameraTarget(0.137, 0.811, -21.002, "LINEAR", 0, false, 0)
  SetEnv_All("c1.0", 0)
  FIELD_SET()
  NpcInvisible(FOR_ALL)
  EVENT_START()
  CUT("1.0")
  Fade_In(0, FADE_TIME)
  WaitFrame(FADE_TIME)
  MessageTalk("m140_030_010")
  MessageClose()
  WaitFrame(10)
  MessageTalk("m140_030_020")
  MessageClose()
  WaitFrame(10)
