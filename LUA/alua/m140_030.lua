      PlayMotion("chr009_01", "fe04", 2, false, 0)
      PlayMotion("chr009_01", "bn01", 10, true, 58)
      MessageTalk("m140_030_100")
      MessageClose()
      WaitFrame(10)
      MessageTalk("m140_030_110")
      MessageClose()
      WaitFrame(10)
