  LoadEnvironmentFile("c3.0", "es_m140_030_c3.0")
  LoadEnvironmentFile("c3.0_A1", "es_m140_030_c3.0_A1")
  LoadEnvironmentFile("c3.0_B1", "es_m140_030_c3.0_B1")
  LoadEnvironmentFile("c3.0_C1", "es_m140_030_c3.0_C1")
  WaitVistaLoad()
  SetPosition("HERO_01", 0.595, 0, -19.2, "LINEAR", 0, 0)
  SetRotation("HERO_01", 0, -165, 0, "LINEAR", 0, 0)
  SetPosition("chr009_01", 0.133, 0.2, -21.028, "LINEAR", 0, 0)
