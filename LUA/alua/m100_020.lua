require("include")

function m100_020()
  Fade_Out(0, 0)
  EVENT_BEGIN()
  Talk.Load("m100")
  SetEventCutNo(5)
  if Debug.IsEventView() then
    SetTimeZone("Noon")
    SetTimeAxis("X2028_daft_10")
    LoadMap("t0303")
  end
  LoadModel("HERO_01", "Player")
  LoadModel("HEROINE_01", "char_HEROINE")
  LoadModel("AEGIOMON_B_01", "char_AEGIOMON_PLAIN_CLOTHES")
  LoadModel("DIGI_MINERVAMON_01", "char_MINERVAMON")
  LoadSceneViewerResource("c1.0", "t0303f_pa")
  LoadSceneViewerResource("c2.0", "t0303f_pa")
  LoadSceneViewerResource("c2.0_A1", "t0303f_pa")
  LoadSceneViewerResource("c2.0_B1", "t0303f_pa")
  LoadSceneViewerResource("c2.0_C1", "t0303f_pa")
  LoadEnvironmentFile("c1.0", "es_m100_020_c1.0")
  LoadEnvironmentFile("c2.0", "es_m100_020_c2.0")
  LoadEnvironmentFile("c2.0_A1", "es_m100_020_c2.0_A1")
  LoadEnvironmentFile("c2.0_B1", "es_m100_020_c2.0_B1")
  LoadEnvironmentFile("c2.0_C1", "es_m100_020_c2.0_C1")
  WaitVistaLoad()
  SetPosition("HERO_01", 1.3, 0.057, 0.15, "LINEAR", 0, 0)
  SetRotation("HERO_01", 0, 80, 0, "LINEAR", 0, 0)
  SetScale("HERO_01", 1, 1, 1)
  SetPosition("HEROINE_01", 2.539, 0.112, 0.41, "LINEAR", 0, 0)
  SetRotation("HEROINE_01", 0, -100, 0, "LINEAR", 0, 0)
  SetScale("HEROINE_01", 1, 1, 1)
  SetPosition("AEGIOMON_B_01", 1.49, 0.065, 1.247, "LINEAR", 0, 0)
  SetRotation("AEGIOMON_B_01", 0, 150, 0, "LINEAR", 0, 0)
  SetScale("AEGIOMON_B_01", 1, 1, 1)
  SetPosition("DIGI_MINERVAMON_01", 1.931, 0.084, -0.98, "LINEAR", 0, 0)
  SetRotation("DIGI_MINERVAMON_01", 0, 35, 0, "LINEAR", 0, 0)
  SetScale("DIGI_MINERVAMON_01", 0.73, 0.73, 0.73)
  PlayMotion("HERO_01", "fn01_01", 0, true, 0)
  SetFace("HERO_01", "F01", 0, 1, 0, 0)
  PlayMotion("HEROINE_01", "e008", 0, true, 0)
  SetFace("HEROINE_01", "F04", 0, 1, 0, 0)
  PlayMotion("AEGIOMON_B_01", "fn01_01", 0, true, 0)
  SetFace("AEGIOMON_B_01", "F01", 0, 1, 0, 0)
  PlayMotion("DIGI_MINERVAMON_01", "fn01_01", 0, true, 0)
  SetFace("DIGI_MINERVAMON_01", "F01", 0, 1, 0, 0)
  BlendEye("HERO_01", 0, 0, "SCURVE", 0, 0)
  BlendFace("HERO_01", 0, 0, 0, true, "SCURVE", 0, 0)
  BlendEye("HEROINE_01", 0, 0, "SCURVE", 0, 0)
  BlendFace("HEROINE_01", 0, 0, 0, true, "SCURVE", 0, 0)
  BlendEye("AEGIOMON_B_01", 0, 0, "SCURVE", 0, 0)
  BlendFace("AEGIOMON_B_01", -60, -20, 20, true, "SCURVE", 0, 0)
  BlendFaceDigi("DIGI_MINERVAMON_01", 0, 0, 0, "SCURVE", 0, 0)
  SetCamera(-3.011, 1.398, 2.153, 0, 40, "LINEAR", 0, false, 0)
  SetCameraTarget(2.07, 1.046, 0.059, "LINEAR", 0, false, 0)
  SetEnv_All("c1.0", 0)
  FIELD_SET()
  EVENT_START()
  CUT("1.0")
  SetCamera(-2.803, 1.384, 2.067, 0, 40, "LINEAR", 120, true, 0)
  SetCameraTarget(2.07, 1.046, 0.059, "LINEAR", 120, true, 0)
  Fade_In(0, FADE_TIME)
  WaitFrame(FADE_TIME)
  MessageTalk("m100_020_010")
  MessageClose()
  WaitFrame(10)
  CAM_POS_WAIT(60)
  CUT("2.0")
  correct = false
  while not Vista.IsSkipping() and not correct do
    SetEnv_All("c2.0", 0)
    SetCamera(1.923, 1.593, 1.01, 0, 30, "LINEAR", 0, false, 0)
    SetCameraTarget(1.368, 1.568, 0.233, "LINEAR", 0, false, 0)
    PlayMotion("HERO_01", "fn01_01", 0, true, 0)
    SetFace("HERO_01", "F01", 0, 1, 0, 0)
    PlayMotion("HEROINE_01", "fn01_01", 0, true, 0)
    SetFace("HEROINE_01", "F01", 0, 1, 0, 0)
    MessageTalkSel(3, "m100_020_020")
    result = Talk.GetResultSelectedNum()
    if result == RESULT_TALK00 then
      PlayMotion("HERO_01", "e002", 2, true, 0)
      SetFace("HERO_01", "F16", 0, 0, FACE_BLENDTIME, 0)
      SetFace("HERO_01", "F16", 0, 1, FACE_BLENDTIME, 60)
      WaitFrame(65)
      CUT("2.0_A1")
      SetEnv_All("c2.0_A1", 0)
      SetCamera(1.692, 1.546, 1.054, 0, 30, "LINEAR", 0, false, 0)
      SetCameraTarget(2.493, 1.546, 0.424, "LINEAR", 0, false, 0)
      SetRotation("HEROINE_01", 0, -73, 0, "LINEAR", 0, 0)
      PlayMotion("HEROINE_01", "e008", 0, true, 0)
      SetStartFrame("HEROINE_01", "e008", 66, 0)
      SetFace("HEROINE_01", "E13_M13", 0, 1, 0, 0)
      BlendFace("HEROINE_01", 30, 15, 0, true, "SCURVE", 0, 0)
      MessageTalk("m100_020_030")
      MessageClose()
      WaitFrame(10)
      EndMotion("HEROINE_01", 0)
      SetFace("HEROINE_01", "F02", 0, 1, FACE_BLENDTIME, 0)
      SetRotation("HEROINE_01", 0, -100, 0, "LINEAR", 20, 0)
      BlendFace("HEROINE_01", 0, 38, 0, true, "SCURVE", 20, 0)
      MessageTalk("m100_020_040")
      MessageClose()
      WaitFrame(10)
      ENDMOTION_WAIT("HEROINE_01", 0)
      correct = true
    elseif result == RESULT_TALK01 then
      PlayMotion("HERO_01", "e008", 2, true, 0)
      SetFace("HERO_01", "F06", 0, 0, FACE_BLENDTIME, 0)
      SetFace("HERO_01", "F06", 0, 1, FACE_BLENDTIME, 60)
      WaitFrame(65)
      CUT("2.0_B1")
      SetEnv_All("c2.0_B1", 0)
      SetCamera(1.692, 1.546, 1.054, 0, 30, "LINEAR", 0, false, 0)
      SetCameraTarget(2.493, 1.546, 0.424, "LINEAR", 0, false, 0)
      PlayMotion("HEROINE_01", "e008_01", 2, true, 0)
      SetFace("HEROINE_01", "E06_M04", 0, 1, FACE_BLENDTIME, 0)
      BlendEye("HEROINE_01", 0, 0, "SCURVE", 0, 0)
      BlendFace("HEROINE_01", 0, 38, 0, true, "SCURVE", 0, 0)
      MessageTalk("m100_020_050")
      MessageClose()
      WaitFrame(10)
    elseif result == RESULT_TALK02 then
      PlayMotion("HERO_01", "e004", 2, true, 0)
      SetFace("HERO_01", "F08", 0, 0, FACE_BLENDTIME, 0)
      SetFace("HERO_01", "F08", 0, 2, FACE_BLENDTIME, 60)
      WaitFrame(65)
      CUT("2.0_C1")
      SetEnv_All("c2.0_C1", 0)
      SetCamera(1.692, 1.546, 1.054, 0, 30, "LINEAR", 0, false, 0)
      SetCameraTarget(2.493, 1.546, 0.424, "LINEAR", 0, false, 0)
      PlayMotion("HEROINE_01", "e033", 2, true, 0)
      SetFace("HEROINE_01", "E10_M01", 0, 1, FACE_BLENDTIME, 0)
      PlayMotionBlend("HEROINE_01", "blend03", 0, false, 0)
      SetBlink("HEROINE_01", 2, 1, 70)
      BlendEye("HEROINE_01", 0, 0, "SCURVE", 0, 0)
      BlendFace("HEROINE_01", 0, 38, 0, true, "SCURVE", 0, 0)
      MessageTalk("m100_020_060")
      MessageClose()
      WaitFrame(10)
      EndMotion("HEROINE_01", 0)
      PlayMotionBlend("HEROINE_01", "blend03", 0, false, 60)
      SetFace("HEROINE_01", "F01", 0, 1, FACE_BLENDTIME, 0)
      SetBlink("HEROINE_01", 0, 1, 0)
      MessageTalk("m100_020_070")
      MessageClose()
      WaitFrame(10)
      ENDMOTION_WAIT("HEROINE_01", 0)
    end
  end
  PlayMotion("HEROINE_01", "e032", 2, true, 0)
  MessageTalk("m100_020_080")
  MessageClose()
  WaitFrame(10)
  EndMotion("HEROINE_01", 0)
  ENDMOTION_WAIT("HEROINE_01", 0)
  Fade_OutNoLoadingWithWait(FADE_BLACK, FADE_TIME)
  EVENT_END()
end

function CUT(cut_no)
  CUT_START(cut_no)
end

function main()
  m100_020()
end
