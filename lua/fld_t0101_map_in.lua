require("include")

function fld_t0101_map_in()
  Fade_Out(0, 0)
  EVENT_BEGIN()
  SetTimeZone("Noon")
  FldSetTimeAxis(PRESENT)
  SetMapStatus("Disable")
  if Debug.IsEventView() then
    LoadMap("t0101")
  end
  SetEventCutNo(3)
  LoadModel("HERO_01", "Player")
  WaitVistaLoad()
  SetPosition("HERO_01", 1.168, 0.369, 32.38, "LINEAR", 0, 0)
  SetRotation("HERO_01", 0, -155, 0, "LINEAR", 0, 0)
  SetScale("HERO_01", 1, 1, 1)
  PlayMotion("HERO_01", "fn01_01", 0, false, 0)
  PlayMotionFace("HERO_01", "", "", 0, 0)
  SetBlink("HERO_01", 0, 1, 0)
  SetMouth("HERO_01", 1, 0, 0)
  BlendEye("HERO_01", 0, 0, "SCURVE", 0, 0)
  BlendFace("HERO_01", 0, 0, 0, false, "SCURVE", 0, 0)
  SetCamera(-33.099, 13.913, 11.025, 0, 60, "ACCEL", 0, false, 0)
  SetCameraTarget(-4.998, 17.873, -27.837, "ACCEL", 0, false, 0)
  Field_InvisibleFollowerAllPartyMember(INVISIBLE_KEY_EVENT, 0, false)
  EVENT_START()
  CUT(1)
  SetCamera(-28.979, 13.913, 11.025, 0, 60, "LINEAR", 180, true, 0)
  SetCameraTarget(-0.878, 17.873, -27.837, "LINEAR", 180, true, 0)
  Fade_In(0, 30)
  WaitFrame(180)
  CUT(2)
  SetCamera(8.236, 6.261, 10.567, 0, 45, "LINEAR", 0, true, 0)
  SetCameraTarget(-4.433, 4.398, -7.764, "LINEAR", 0, true, 0)
  SetCamera(8.469, 4.218, 10.613, 0, 45, "DECEL", 180, false, 0)
  SetCameraTarget(-4.2, 2.355, -7.718, "DECEL", 180, false, 0)
  WaitFrame(180)
  CUT(3)
  Field.ResetPlayer(1.168, 0.369, 32.38, -25)
  SetCamera(-0.662, 5.89, 23.537, 0, 45, "ACCEL", 0, false, 0)
  SetCameraTarget(-0.81, 7.432, 35.098, "ACCEL", 0, false, 0)
  SetCamera(0.711, 2.532, 27.985, 0, 45, "DECEL", 180, false, 0)
  SetCameraTarget(0.62, 1.162, 35.098, "DECEL", 180, false, 0)
  WaitFrame(100)
  Field_CancelInvisibleFollowerAllPartyMember(INVISIBLE_KEY_EVENT, 50, false, false)
  WaitFrame(90)
  EVENT_END_SEAMLESS(60)
end

function CUT(cut_no)
  CUT_START()
end

function main()
